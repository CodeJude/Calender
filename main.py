# Python program to print Calendar

# Python built-in calender function
import calendar

# Enter in format - 2022, 2010, 1998 etc.
year = int(input("Enter Year: "))

# Enter in format - 03, 12, 01 etc.
month = int(input("Enter Month: "))

# Calendar to be printed
print(calendar.month(year, month))

